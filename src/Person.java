
public class Person 
{
	int titleId;
	int ordering;
	int nconst;
	String category;
	String job;
	String[] characters;
	
	public Person(int titleId, int ordering, int nconst, String category, String job, String[] characters) 
	{
		this.titleId = titleId;
		this.ordering = ordering;
		this.nconst = nconst;
		this.category = category;
		this.job = job;
		this.characters = characters;
	}
}
